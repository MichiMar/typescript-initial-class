// +, -, *, /, %, ++, --

var numOne: number = 1;
var numTwo: number = 2;

// Addition
console.log("Addition");
console.log(numOne + numTwo);

// Substraction
console.log("Substraction");
console.log(numOne - numTwo);

// Multiplication
console.log("Multiplication");
console.log(numOne * numTwo);

// Division
console.log("Division");
console.log(numOne / numTwo);

// Modulus
console.log("Modulus:");
var numThree: number = 2;
var numFour: number = 21;
console.log(numFour % numThree);

// Increment
console.log("Increment:");
var x: number = 0;
while (x < 10) {
  console.log(x);
  x++;
}

// Decrement
console.log("Dencrement:");
var x: number = 10;
while (x > 0) {
  console.log(x);
  x--;
}
